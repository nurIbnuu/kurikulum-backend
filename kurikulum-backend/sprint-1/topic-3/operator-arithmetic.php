<?php
// Penjumlahan (+)
$jumlah = 1;
$hasil = $jumlah + 2;
echo $hasil . PHP_EOL;

// Pengurangan (-)
$kurang = 3;
$hasil1 = $kurang - $jumlah;
echo $hasil1 . PHP_EOL;

// Perkalian (*)
$kali = 2;
$hasil2 = $kali * $kurang;
echo $hasil2 . PHP_EOL;

// Pembagian (/)
$bagi = 10;
$hasil3 = $bagi / $kali;
echo $hasil3 . PHP_EOL;

// Modulus/Sisa Bagi (%)
$mod = 4;
$hasil4 = $mod % $kali;
echo $hasil4 . PHP_EOL;

// Pangkat (**)
$pangkat = 2;
$hasil5 = $pangkat ** $kurang;
echo $hasil5 . PHP_EOL;